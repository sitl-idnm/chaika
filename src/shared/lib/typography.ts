/**
 * 校斜懈褉邪械褌 芦胁懈褋褟褖懈械禄 锌褉械写谢芯谐懈/泻芯褉芯褌泻懈械 褋谢芯胁邪: 锌褉懈泻谢械懈胁邪械褌 泻 褋谢械写褍褞褖械屑褍 褋谢芯胁褍
 * 薪械褉邪蟹褉褘胁薪褘屑 锌褉芯斜械谢芯屑 (U+00A0) 谢褞斜褘械 1-2-褋懈屑胁芯谢褜薪褘械 褋谢芯胁邪 (胁, 芯, 懈, 薪邪, 蟹邪...),
 * 褑懈褎褉褘 懈 褔邪褋褌褘械 3-斜褍泻胁械薪薪褘械 锌褉械写谢芯谐懈/褋芯褞蟹褘 (写谢褟, 懈谢懈, 锌芯写...).
 *
 * 袩褉懈屑械薪褟械褌褋褟 泻 锌芯谢褜蟹芯胁邪褌械谢褜褋泻芯屑褍 褌械泻褋褌褍 胁 泻芯屑锌芯薪械薪褌邪褏: {nbp('褌械泻褋褌')}.
 */
const SHORT_WORDS =
  /(^|[\s(芦"鈥瀅)([邪-褟褢a-z袗-携衼A-Z0-9]{1,2}|写谢褟|懈谢懈|锌芯写|薪邪写|锌褉懈|斜械蟹|锌褉芯|褔褌芯|泻邪泻|褝褌芯|褍卸械|械谐芯|械屑褍|芯斜芯|懈蟹芯)([\s])/g

export function nbp(text: string): string {
  let prev = ''
  let out = text
  // 锌芯胁褌芯褉褟械屑, 褔褌芯斜褘 褋泻谢械懈褌褜 褑械锌芯褔泻懈 泻芯褉芯褌泻懈褏 褋谢芯胁 (芦邪 褟 胁 谢械褋禄)
  while (out !== prev) {
    prev = out
    out = out.replace(SHORT_WORDS, '$1$2聽')
  }
  return out
}
